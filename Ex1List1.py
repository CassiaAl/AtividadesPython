def main():
    # Solicita o valor da base do retângulo
    base = float(input("Digite o valor da base do retângulo: "))

    # Solicita o valor da altura do retângulo
    altura = float(input("Digite o valor da altura do retângulo: "))

    # Calcula a área do retângulo
    area = base * altura

    # Exibe a área do retângulo
    print(f"A área do retângulo é: {area}")


if __name__ == "__main__":
    main()
